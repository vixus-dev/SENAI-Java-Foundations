package Lição_1;

public class Exercício5 {

	public static void main(String[] args) {
        System.out.println("Linha 1: Iniciando o loop...");

        // Linha 2: O loop 'for'
        for (int contador = 3; contador <= 9; contador+=3) {
            // Linha 3: Este bloco será executado repetidamente
            System.out.println("Linha 4: Contagem atual: " + contador);
        }

        System.out.println("Linha 5: Loop finalizado.");
    }
}
