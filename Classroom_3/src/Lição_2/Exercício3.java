package Lição_2;

public class Exercício3 {

	public static void main(String[] args) {
		 // Declaração e inicialização da idade em anos (tipo int)
        int idadeAnos = 28;

        // Definição de dias por ano (tipo int)
        int diasPorAno = 365;

        // Cálculo da idade em dias (o resultado também será int)
        int idadeDias = idadeAnos * diasPorAno;

        // Impressão do resultado
        System.out.println("A idade em anos é: " + idadeAnos);
        System.out.println("A idade aproximada em dias é: " + idadeDias);


	}

}
