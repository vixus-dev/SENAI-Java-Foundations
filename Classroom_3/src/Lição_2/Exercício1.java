public class Exercício1 {

	   public static void main(String[] args) {
	        // Declaração e inicialização de duas variáveis do tipo int
	       int numero1 = 10;
	       int numero2 = 25;

	        // Cálculo da soma
	       int soma = numero1 + numero2;

	        // Impressão do resultado
	       System.out.println("O primeiro número é: " + numero1);
	       System.out.println("O segundo número é: " + numero2);
	       System.out.println("A soma de " + numero1 + " e " + numero2 + " é: " + soma);
	       
	  }
}

