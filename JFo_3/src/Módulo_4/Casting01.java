package Módulo_4;

public class Casting01 {

	public static void main(String[] args) {
		
		// byte a = 128;
		short b = (byte) 128;
		System.out.print("Variavel 'b' tem o valor de: ");
		System.out.println(b);
		
		byte c = 127;
		c++;
		System.out.print("Após adicionar 1 a variavel 'c' ela fica: ");
		System.out.println(c);
		c++;
		System.out.print("Após adicionar mais 1 a variavel 'c' ela fica: ");
		System.out.println(c);
		
	}
}
