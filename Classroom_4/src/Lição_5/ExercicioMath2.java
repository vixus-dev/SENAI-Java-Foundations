package Lição_5;

public class ExercicioMath2 {

	public static void main(String[] args) {
		
		double numeroDecimal = 8.7;
		double numeroNegativo = -12.3;
		double numeroMeio = 4.5;
		
		//para o Numero decimal :
		System.out.println("Numero decimal");
		System.out.println(numeroDecimal);
		System.out.println(Math.ceil(numeroDecimal));
		System.out.println(Math.floor(numeroDecimal));
		System.out.println(Math.round(numeroDecimal));
		
		//para o Numero Negativo
		System.out.println("Numero Negativo");
		System.out.println(numeroNegativo);
		System.out.println(Math.ceil(numeroNegativo));
		System.out.println(Math.floor(numeroNegativo));
		System.out.println(Math.round(numeroNegativo));
		
		//para o numero Meio
		System.out.println("Numero Meio");
		System.out.println(numeroMeio);
		System.out.println(Math.ceil(numeroMeio));
		System.out.println(Math.floor(numeroMeio));
		System.out.println(Math.round(numeroMeio));
		
	}
}
